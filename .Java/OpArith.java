class OpArith 
{
	public static void main(String[] args) 
	{
		int n1 = 7, n2 = 3;
		System.out.println("오 써쎀 : " + (n1 + n2));   // 10
		System.out.println("짋 써쎀 : " + (n1 - n2));   // 4
		System.out.println("썼 써쎀 : " + (n1 * n2));   // 21
		System.out.println("씱얋 써쎀 : " + (n1 / n2));   // 2
		System.out.println("씱쟵쵔 써쎀 : " + (n1 % n2));   // 1
	}
}